def all_ch_upper(_str: str) -> str:
    """
    Возвращает строку со всеми заглавными буквами
    :param _str:
    :return:
    """
    return _str.upper()


def upper_first_ch(_str: str) -> str:
    """
    Возвращает строку с заглавными первыми буквами каждого слова
    :param _str: str
    :return: str
    """
    return _str.title()
